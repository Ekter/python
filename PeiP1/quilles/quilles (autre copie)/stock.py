    circle(t*500,7)
    down()
    right(17)
    circle(t*-500,13)
    circle(t*800,7)
    right(10)
    circle(t*-800,7)
    up()
    right(90)
    forward(t*40)
    right(90)
    backward(t*20)
    down()
    circle(t*1660,5)
    
    pencolor('#553c11')
    up()
    backward(t*65)
    left(90)
    forward(t*-40)
    down()
    write("RUPTURE",font=("Arial", int(t*50), "normal"))
    up()
    backward(t*100)
    right(90)
    forward(t*50)
    down()
    write("STOCK",font=("Arial", int(t*50), "normal"))
    up()
    forward(t*70)
