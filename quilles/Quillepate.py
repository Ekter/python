    tortuepate.down()
    tortuepate.fillcolor('#e2261a')
    tortuepate.begin_poly()
    tortuepate.right(20)
    tortuepate.circle(t*-60,70)
    tortuepate.left(3)
    tortuepate.forward(t*70)
    tortuepate.circle(t*-90,30)
    tortuepate.left(126)
    tortuepate.forward(t*28)
    tortuepate.circle(t*10,80)
    tortuepate.right(15)
