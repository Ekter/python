    tortuepate.forward(t*70)
    tortuepate.circle(t*-90,32)
    tortuepate.right(45)
    tortuepate.circle(t*-1600,8.4)
    tortuepate.right(78)
    tortuepate.circle(t*-75,45)
    tortuepate.circle(t*30,50)
    tortuepate.right(15)
    tortuepate.circle(t*350,15)
    tortuepate.circle(t*-30,30)
    tortuepate.left(50)
