    tortuepate.forward(t*25)
    tortuepate.circle(t*6,180)
    tortuepate.up()
    tortuepate.goto(g,h)
    tortuepate.down()
    tortuepate.left(40)
    tortuepate.circle(t*-15,70)
    tortuepate.left(152)
    tortuepate.circle(t*700,10)
    tortuepate.circle(t*-200,17)
