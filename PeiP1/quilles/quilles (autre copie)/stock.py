from turtle import setheading
from setup import *

def stock(taille,x,y):

    up()
    goto(x,y)
    down()
    setheading(0)

    t=taille/10

    tracer(0)

    up()
    
    backward(t*245)
    right(90)
    forward(t*92)
    left(90)
    down()

    #Planche
    fillcolor('#d49335')
    begin_fill()
    forward(t*490)
    left(50)
    forward(t*16)
    left(50)
    for i in range(2):
        forward(t*40)
        right(20)
        forward(t*40)
        left(20)
    left(20)
    forward(t*16)
    left(60)
    forward(t*500)
    left(45)
    forward(t*10)
    left(53)
    circle(t*-800,10.5)
    right(50)
    forward(t*7)
    left(70)
    forward(t*30)
    end_fill()
    #Contour relief
    fillcolor('#6e4320')
    begin_fill()
    right(150)
    forward(t*6)
    right(30)
    forward(t*30)
    right(70)
    forward(t*7)
    left(50)
    circle(t*800,10.3)
    right(53)
    forward(t*10)
    right(53)
    forward(t*6)
    right(128)
    forward(t*10)
    left(53)
    circle(t*-800,10.5)
    right(50)
    forward(t*7)
    left(70)
    forward(t*30)
    end_fill()
    up()
    left(74)
    forward(t*495)
    right(30)
    down()
    begin_fill()
    forward(t*6)
    left(80)
    forward(t*16)
    left(50)
    for i in range(2):
        forward(t*40)
        right(20)
        forward(t*40)
        left(20)
    forward(t*7)
    left(20)
    forward(t*14)
    w,d=pos()
    left(80)
    forward(t*4)
    left(95)
    forward(t*16)
    right(35)
    for i in range(2):
        forward(t*40)
        left(20)
        forward(t*40)
        right(20)
    right(30)
    forward(t*16)
    g,h=pos()
    end_fill()
    up()
    goto(w,d)
    right(50.2)
    down()
    begin_fill()
    forward(t*505)
    left(160)
    forward(t*6)
    left(20)
    forward(t*490)
    end_fill()
    up()
    goto(g,h)
    down()
    begin_fill()
    left(180)
    forward(t*500)
    left(160)
    forward(t*6)
    left(20)
    forward(t*490)
    end_fill()
    pencolor('#c1772c')
    width(3)
    up()
    left(90)
    forward(t*10)
    left(80)
    down()
    circle(t*1400,20)
    up()
    right(90)
    forward(t*50)
    right(90)
    forward(t*30)
    right(10)
    down()
    circle(t*1400,10)
    right(5)
    circle(t*-1660,6)
    circle(t*1000,3)
    up()
    left(90)
    forward(t*50)
    left(90)
    forward(t*20)
    down()
    circle(t*1000,15)
